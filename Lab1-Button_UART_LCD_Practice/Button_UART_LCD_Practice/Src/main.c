// operation to Change MODE 1
void GoToMode1(void)
{
	Mode = MODE_1;	
	BSP_LED_Off(LED1);
	Stop_LED_Timer();
	PrintModeAtXY(0, 0);
	UART_TX_Mode();
}

// operation to Change MODE 2
void GoToMode2(void)
{
	Mode = MODE_2;
	Start_LED_Timer();
	PrintModeAtXY(0, 0);
	UART_TX_Mode();
}

// Print current Mode
void PrintModeAtXY(uint32_t x, uint32_t y)
{
	uint8_t PrintBuffer[15] = "LED FLASH=0";
	PrintBuffer[10] = '0' + Mode;
	BSP_LCD_DisplayStringAt(x, y, PrintBuffer,CENTER_MODE);
}


// Check Button
uint32_t IsButtonPressed(void)
{
	uint32_t Pressed = 0;
	static uint32_t PrvBtnState = RESET;
	uint32_t CurrentBtnState = BSP_PB_GetState(BUTTON_KEY);;
	
	if (PrvBtnState == SET && CurrentBtnState == RESET){
		Pressed = 1;
	}
	
	PrvBtnState = CurrentBtnState;
	return Pressed;
}

// UART Transmit flash = 0
void UART_TX_Mode(void)
{
	uint8_t PrintBuffer[15] = "LED FLASH=0\r\n";
	PrintBuffer[10] = '0' + Mode;
	HAL_UART_Transmit(&huart1, (uint8_t *)PrintBuffer, 13, 500);
}

void UART_TX_Introduction(void)
{
	HAL_UART_Transmit(&huart1, (uint8_t *)"Btn UART LCD - B10602225\r\n", 26, 500);
}

